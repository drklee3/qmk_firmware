bool encoder_update_user(uint8_t index, bool clockwise) {
    if (index == 0) {
        if (clockwise) {
            if (shift_held) {
                tap_code(KC_MNXT);
            } else {
                tap_code(KC_RIGHT);
            }
        } else {
            if (shift_held) {
                tap_code(KC_MPRV);
            } else {
                tap_code(KC_LEFT);
            }
        }
    } else if (index == 1) {
        if (clockwise) {
            if (shift_held) {
                rgblight_increase_hue();
            } else {
                tap_code(KC_DOWN);
            }
        } else {
            if (shift_held) {
                rgblight_decrease_hue();
            } else {
                tap_code(KC_UP);
            }
        }
    }
    return true;
}

